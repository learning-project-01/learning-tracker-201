public class OneDArraForLoop
{

        public static void main(String[] args)
        {
            int[] intArray = {10, 20, 30, 40, 50};

            System.out.println("Using for index loop:");
            for (int i = 0; i < intArray.length; i++) {
                System.out.println(intArray[i]);
            }
        }
}
